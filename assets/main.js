	answerArray = [["The Piano", "Schindler's List", "In the Name of the Father", "The Fugitive"], ["Key Largo","The African Queen","Casablanca","The Big Sleep"], ["The Unforgiven", "Premonition", "The Uninvited", "The Sixth Sense"], ["Harrison Ford","Mel Gibson","Kevin Kline","Tom Hanks"], ["Rain Man", "Little Big Man", "Tootsie", "Sleepers"], ["Halloween","Friday the 13th","Nightmare on Elm Street","Psycho"], ["One more Strike", "King Pin", "Gutter Balls", "PBA"], ["$2","$150","$6","$20"]],
	correctAnswers = ["B. Schindler's List", "C. Casablanca", "D. The Sixth Sense", "A. Harrison Ford", "C. Tootsie", "B. Friday the 13th", "B. King Pin", "A. $2"],
	questionCounter = 0,
	selecterAnswer,
	theClock,
	correctTally = 0,
